// Declaração das variáveis numeroUm e stringUm com valores diferentes, mas equivalentes
let numeroUm = 1
let stringUm = '1'

// Comparação entre numeroUm e stringUm usando o operador de igualdade ==
if (numeroUm == stringUm) {
  // Se a comparação for verdadeira, exibe uma mensagem indicando que as variáveis têm o mesmo valor, mas tipos diferentes
  console.log('As variáveis numeroUm e stringUm têm o mesmo valor, mas tipos diferentes');
} else {
  // Se a comparação for falsa, exibe uma mensagem indicando que as variáveis não têm o mesmo valor
  console.log('As variáveis numeroUm e stringUm não têm o mesmo valor');
}

